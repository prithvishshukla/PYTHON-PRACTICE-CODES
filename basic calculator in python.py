# Input two numbers from the user
a = float(input("Enter number a: "))
b = float(input("Enter number b: "))

# Print the numbers entered by the user
print("Number a is:", a)
print("Number b is:", b)

# Perform and print various arithmetic operations
print("Sum of a and b is:", a + b)              # Addition
print("Difference of a and b is:", a - b)       # Subtraction
print("Product of a and b is:", a * b)          # Multiplication
print("Division of a and b is:", a / b)         # Division
print("Floor division of a and b is:", a // b)  # Floor Division
print("Remainder when a is divided by b is:", a % b)  # Modulus

# Perform and print power calculations
print("a raised to the power b is:", a ** b)    # Exponentiation a^b
print("b raised to the power a is:", b ** a)    # Exponentiation b^a
print("a raised to the power 1/b is:", a ** (1 / b))  # Root a^(1/b)
print("b raised to the power 1/a is:", b ** (1 / a))  # Root b^(1/a)

# It takes two numbers as input and performs basic arithmetic operations on them.
# It then prints the results of these operations.
# The code uses the input() function to take user input and the print() function to display the results.
# The code also uses the float() function to convert the input strings to floating-point numbers.
